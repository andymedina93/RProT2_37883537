                <div class="cont-central">Somos una empresa innovadora en el mercado Corrientes y la        región. "Lencería RM" es una empresa familiar con varios años en plaza.<br/><br/>

                Nos iniciamos con un local en la ciudad de corrientes capital, y crecimos gracias a la confianza de todos los clientes. Ampliamos nuestra red de ventas y diseñamos un nuevo modelo de negocios, innovador. Cómodo para nuestros clientes.<br/>
                Actualmente tenemos nuestra Casa central en Resistencia Chaco y dos sucursales en Corrientes,<br/><br/>

                Iniciamos nuestras actividades comercializando los rubros de corsetería y lencería. Con el paso del tiempo, evolucionamos como pequeña empresa; nos expandimos, y ahora ofrecemos servicios de venta en el mercado mayorista, únicamente. <br/><br/>

                El crecimiento de <b>"Lencería RM Distribuidora Mayorista"</b> está basado en el permanente esfuerzo, creatividad y conocimiento que aportamos al emprendimiento comercial. Y la experiencia adquirida, nos permite ofrecerle varias modalidades de comercialización; convencidos de conocer las necesidades, criterios y demandas de nuestros clientes. <br/><br/>

                La política comercial de Lencería Cice, está basada en innovar. Por ello facilitamos las condiciones para que Usted pueda operar con nosotros eficientemente a través de Internet, con este sitio. 
                    
                Desde <b>Distribuidora RM</b> acompañaremos su emprendimiento comercial. Para ello contamos con un "stock que incluye las líneas completas de las primeras marcas en el mercado argentino en lencería, pijamas y trajes de baño". Ello incluye también la ropa interior para hombres y niños. <br/><br/>

                Con el correr del tiempo, nos convertimos en referentes de un modelo de negocio que prioriza a nuestros clientes y respecto de la moda, nuestros niveles de venta nos permiten gozar de la confianza de la exclusividad de grandes marcas que confian su prestigio en el nuestro. Y de esa forma también, de alguna manera, pasamos a convertirnos en referentes de la moda de "ropa íntima" ya que disponemos de productos antes que la competencia. <br/><br/>

                Tenemos "inmejorables precios y garantizamos la entrega de la mercadería que compra, en cualquier punto del país". <br/><br/>

                Esperamos poder servir de la mejor forma a nuestros clientes para facilitar su compra, dándole la mayor comodidad y satisfacción. Sólo de esa manera nuestra misión estará cumplida.<br/><br/>

